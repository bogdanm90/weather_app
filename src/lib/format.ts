export type Units = 'c' | 'f';

export function formatTemperature(temp: number, units: Units): string {
  return `${Math.round(temp)}°${units.toUpperCase()}`;
}

export function convertCtoF(celsius: number): number {
  return (celsius * 9) / 5 + 32;
}

export function convertFtoC(fahrenheit: number): number {
  return ((fahrenheit - 32) * 5) / 9;
}

export function formatDate(dateString: string): string {
  const date = new Date(dateString);
  return date.toLocaleDateString('en-US', {
    weekday: 'short',
    month: 'short',
    day: 'numeric',
  });
}

export function formatTime(dateString: string): string {
  const date = new Date(dateString);
  return date.toLocaleTimeString('en-US', {
    hour: 'numeric',
    minute: '2-digit',
    hour12: true,
  });
}

